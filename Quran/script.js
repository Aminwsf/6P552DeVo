let currentPage = 1;
const totalPages = 604;

const quranPageImg = document.getElementById('quranPage');
const prevPageBtn = document.getElementById('prevPage');
const nextPageBtn = document.getElementById('nextPage');
const suraList = document.getElementById('suraList');

function loadPage(pageNumber) {
    quranPageImg.src = `https://www.mushaf.ma/fahres/page/images/muhammadi/page${pageNumber + 2}.png`;
}

function updateNavigation() {
    prevPageBtn.disabled = currentPage === 1;
    nextPageBtn.disabled = currentPage === totalPages;
}

prevPageBtn.addEventListener('click', () => {
    if (currentPage > 1) {
        currentPage--;
        loadPage(currentPage);
        updateNavigation();
    }
});

nextPageBtn.addEventListener('click', () => {
    if (currentPage < totalPages) {
        currentPage++;
        loadPage(currentPage);
        updateNavigation();
    }
});

function populateSuraList() {
    QuranData.Sura.forEach((sura, index) => {
        if (index === 0) return; // Skip the first empty element
        const li = document.createElement('li');
        li.textContent = `${index}. ${sura[0]} (${sura[2]})`;
        li.addEventListener('click', () => {
            const page = QuranData.Page.find(page => page[0] === index)[1];
            currentPage = page;
            loadPage(currentPage);
            updateNavigation();
        });
        suraList.appendChild(li);
    });
}

// Initialize
loadPage(currentPage);
updateNavigation();
populateSuraList();
